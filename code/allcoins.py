# Recover individual tosses from the cumulative batches
final_sequence_heads = []
final_sequence_tails = []

for group in data_sources:
    heads_cum = group['heads']
    tails_cum = group['tails']
    
    prev_h = 0
    prev_t = 0
    
    # Process each trial step in the batch
    for i in range(len(heads_cum)):
        # Calculate how many heads/tails were added in this specific step
        added_h = heads_cum[i] - prev_h
        added_t = tails_cum[i] - prev_t
        
        # Append to master sequence (as many 1s or 0s as were added)
        # For the line graph, we just need to increment the total count step-by-step
        # We simulate the individual events: Heads first, then Tails (order within step doesn't change overall trend)
        final_sequence_heads.extend([1] * added_h)
        final_sequence_heads.extend([0] * added_t) # Heads don't add to tails count
        
        final_sequence_tails.extend([0] * added_h) # Tails don't add to heads count
        final_sequence_tails.extend([1] * added_t)
        
        prev_h = heads_cum[i]
        prev_t = tails_cum[i]

# Convert to numpy for easy cumulative sum calculation
master_h = np.array(final_sequence_heads)
master_t = np.array(final_sequence_tails)

# Compute the GRAND Cumulative Sums
cum_heads_total = np.cumsum(master_h)
cum_tails_total = np.cumsum(master_t)
trials = np.arange(1, len(cum_heads_total) + 1)

print(f"Total Tosses Reconstructed: {len(trials)}") # Should be 3100
print(f"Final Heads: {cum_heads_total[-1]}")
print(f"Final Tails: {cum_tails_total[-1]}")


fig, ax = plt.subplots(figsize=(12, 7))

# Set chart limits
ax.set_xlim(0, 3100)
max_val = max(cum_heads_total[-1], cum_tails_total[-1])
ax.set_ylim(0, max_val * 1.1)

ax.set_title("Combined Grand Total: Heads vs. Tails (3,100 Tosses)", fontsize=16, fontweight='bold')
ax.set_xlabel('Number of Tosses')
ax.set_ylabel('Cumulative Count')
ax.grid(True, alpha=0.3)

# Initialize lines
line_h, = ax.plot([], [], label='Heads', color='blue', linewidth=2)
line_t, = ax.plot([], [], label='Tails', color='red', linewidth=2)
ax.legend(loc='upper left')

# Stats box
stats_text = ax.text(0.02, 0.85, '', transform=ax.transAxes, 
                     bbox=dict(boxstyle="round", fc="white", ec="gray", alpha=0.8))

# To make the animation file size manageable, we skip frames
# Showing every 20th toss makes it smooth but not too huge
step_size = 20
frames = range(0, len(trials) + 1, step_size)

def update(frame_idx):
    if frame_idx == 0:
        return line_h, line_t, stats_text
        
    # Slice data up to current frame
    current_x = trials[:frame_idx]
    current_h = cum_heads_total[:frame_idx]
    current_t = cum_tails_total[:frame_idx]
    
    # Update lines
    line_h.set_data(current_x, current_h)
    line_t.set_data(current_x, current_t)
    
    # Update text
    h_val = current_h[-1]
    t_val = current_t[-1]
    stats_text.set_text(f'Tosses: {frame_idx}\nHeads: {h_val}\nTails: {t_val}')
    
    return line_h, line_t, stats_text

# Create Animation
ani = animation.FuncAnimation(fig, update, frames=frames, interval=20, blit=True)

plt.tight_layout()
plt.show()